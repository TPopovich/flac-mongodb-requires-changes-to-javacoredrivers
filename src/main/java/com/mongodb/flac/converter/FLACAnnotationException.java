package com.mongodb.flac.converter;

/**
 * FLAC Annotation Exception
 */
public class FLACAnnotationException extends Exception {

    public FLACAnnotationException() {
        super();
    }

    public FLACAnnotationException(String s) {
        super(s);
    }

    public FLACAnnotationException(String s, Throwable throwable) {
        super(s, throwable);
    }

    public FLACAnnotationException(Throwable throwable) {
        super(throwable);
    }

}
